                    pst.setString(12, kidney);
                    pst.setString(13, mental);
                    pst.setString(14, backache);
                    pst.setString(15, recurrent);
                    pst.setString(16, migraine);
                    pst.setString(17, surgical);
                    pst.setString(18, gyna);
